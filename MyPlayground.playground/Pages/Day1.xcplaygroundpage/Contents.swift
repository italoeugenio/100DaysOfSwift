import Foundation
import Cocoa

// Variables and Constants
var myVariable = 42
myVariable = 50 // Variable can be changed

let myConstant = 42
// myConstant = 50 // Error: Cannot assign to value: 'myConstant' is a 'let' constant

// Strings
var myString: String = "Hello, Swift!"
let anotherString = "Welcome to Swift programming."

// Numbers
var myInteger: Int = 10
let myDouble: Double = 20.5

// Printing values
print("Variable myVariable: \(myVariable)")
print("Constant myConstant: \(myConstant)")
print("String myString: \(myString)")
print("String anotherString: \(anotherString)")
print("Integer myInteger: \(myInteger)")
print("Double myDouble: \(myDouble)")
